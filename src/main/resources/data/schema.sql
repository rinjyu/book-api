DROP TABLE IF EXISTS BOOK;

CREATE TABLE BOOK (
  BOOK_NO INT AUTO_INCREMENT,
  BOOK_NM VARCHAR(50) NOT NULL,
  BOOK_PRICE INT NOT NULL,
  BOOK_AUTHOR VARCHAR(50) NOT NULL,
  BOOK_PUBLISHER VARCHAR(100) NOT NULL,
  BOOK_DESC VARCHAR(250) NOT NULL,
  BOOK_IMG_PATH VARCHAR(250),
  REG_DTS DATETIME DEFAULT CURRENT_TIMESTAMP,
  REG_ID VARCHAR(15) NOT NULL,
  MOD_DTS DATETIME DEFAULT CURRENT_TIMESTAMP,
  MOD_ID VARCHAR(15) NOT NULL,
  PRIMARY KEY(BOOK_NO)
);

DROP TABLE IF EXISTS MEMBER;

CREATE TABLE MEMBER (
  MEM_ID VARCHAR(15) NOT NULL,
  MEM_NM VARCHAR(15) NOT NULL,
  MEM_PWD VARCHAR(250) NOT NULL,
  MEM_EMAIL VARCHAR(100) NOT NULL,
  REG_DTS DATETIME DEFAULT CURRENT_TIMESTAMP,
  REG_ID VARCHAR(15) NOT NULL,
  MOD_DTS DATETIME DEFAULT CURRENT_TIMESTAMP,
  MOD_ID VARCHAR(15) NOT NULL,
  PRIMARY KEY(MEM_ID)
);